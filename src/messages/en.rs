use super::MsgKey;

pub fn get(key: MsgKey) -> &'static str {
    match key {
        MsgKey::IncludeNotFound          => "Include not found: \"{}\"",
        MsgKey::IncludeTried             => " (also tried: {}.inc)",
        MsgKey::IncludeNoPathsConfigured => ". No includePaths configured.",
        MsgKey::IncludeSearchedIn        => ". Searched in: {}{}",
        MsgKey::IncludeRelativeTo        => ". Relative path from: {}",
        MsgKey::NativeHasBody            => "Native function \"{}\" cannot have a body",
        MsgKey::ForwardHasBody           => "Forward declaration \"{}\" cannot have a body",
        MsgKey::DeclNoBody               => "{} declaration \"{}\" has no body. Use \"forward\" for prototypes.",
        MsgKey::VarUnused                => "\"{}\" variable declared but never used",
        MsgKey::StockUnused              => "\"{}\" stock function declared but never used",
        MsgKey::SymDeprecated            => "\"{}\" is marked as deprecated",
        MsgKey::SymDeprecatedUsage       => "\"{}\" is deprecated",
        MsgKey::SymFromDeprecatedFile    => "\"{}\" belongs to a deprecated include",
        MsgKey::IncludeDeprecated        => "\"{}\" is deprecated",
        MsgKey::ParamUnused              => "Parameter \"{}\" declared but never used",
        MsgKey::SymbolUndeclared         => "\"{}\" is not declared — check that the correct include is present",
        MsgKey::DefineUnused             => "\"{}\" defined but never used",
        MsgKey::IncludeNoSymbolsUsed     => "\"{}\" included but none of its symbols are used",
        MsgKey::TryIncludeNotFound       => "\"{}\" not found — #tryinclude ignored by the compiler",
        MsgKey::NativeNeverCalled        => "\"{}\" native declared but never called",
        MsgKey::ForwardNeverCalled       => "\"{}\" forward declared but never called",
        MsgKey::FuncNeverCalled          => "\"{}\" function declared but never called",
        MsgKey::IndentInconsistent       => "Inconsistent indentation: expected {} columns, found {}",
        MsgKey::RefsZero                 => "0 references",
        MsgKey::RefsOne                  => "1 reference",
        MsgKey::RefsMany                 => "{n} references",
        MsgKey::HoverDeprecated          => "**Deprecated**",
        MsgKey::KwIf                     => "if (condition) { }",
        MsgKey::KwIfElse                 => "if/else",
        MsgKey::KwElse                   => "else",
        MsgKey::KwFor                    => "for (new i = 0; i < n; ++i)",
        MsgKey::KwWhile                  => "while (condition) { }",
        MsgKey::KwDo                     => "do { } while (condition)",
        MsgKey::KwSwitch                 => "switch (value) { case: }",
        MsgKey::KwCase                   => "case value:",
        MsgKey::KwDefault                => "default: (switch)",
        MsgKey::KwReturn                 => "return value",
        MsgKey::KwBreak                  => "break — exit loop/switch",
        MsgKey::KwContinue               => "continue — next iteration",
        MsgKey::KwGoto                   => "goto label",
        MsgKey::KwExit                   => "exit — terminate the script",
        MsgKey::KwNewLocal               => "new local variable",
        MsgKey::KwSizeof                 => "sizeof variable — size",
        MsgKey::KwTagof                  => "tagof variable — numeric tag",
        MsgKey::KwTrue                   => "true (1)",
        MsgKey::KwFalse                  => "false (0)",
        MsgKey::KwCellmax                => "maximum cell value",
        MsgKey::KwCellmin                => "minimum cell value",
        MsgKey::KwCellbits               => "bits per cell",
        MsgKey::KwStock                  => "stock function",
        MsgKey::KwPublic                 => "public function (callback)",
        MsgKey::KwForward                => "forward declaration",
        MsgKey::KwNative                 => "native declaration",
        MsgKey::KwStatic                 => "static function/variable",
        MsgKey::KwEnum                   => "enum declaration",
        MsgKey::KwConst                  => "global constant",
        MsgKey::KwNewGlobal              => "global variable",
        MsgKey::KwDefine                 => "#define macro",
        MsgKey::KwUndef                  => "#undef macro",
        MsgKey::KwInclude                => "#include <file>",
        MsgKey::KwTryinclude             => "#tryinclude <file> (if it exists)",
        MsgKey::KwIfDefined              => "#if defined MACRO … #endif",
        MsgKey::KwIfdef                  => "#ifdef MACRO … #endif",
        MsgKey::KwIfndef                 => "#ifndef MACRO … #endif",
        MsgKey::KwElseDir                => "#else (inside #if)",
        MsgKey::KwEndif                  => "#endif",
        MsgKey::KwPragma                 => "#pragma option",
        MsgKey::KwAssert                 => "#assert condition (compile-time)",
        MsgKey::KwError                  => "#error message (compile-time)",
        MsgKey::KwWarning                => "#warning message (compile-time)",
        MsgKey::KwAtDeprecated           => "Marks the next symbol as deprecated",
        MsgKey::KwLocal                  => "local",
    }
}

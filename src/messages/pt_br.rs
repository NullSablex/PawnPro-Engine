use super::MsgKey;

pub fn get(key: MsgKey) -> &'static str {
    match key {
        MsgKey::IncludeNotFound          => "Include não encontrada: \"{}\"",
        MsgKey::IncludeTried             => " (tentou também: {}.inc)",
        MsgKey::IncludeNoPathsConfigured => ". Nenhum includePaths configurado.",
        MsgKey::IncludeSearchedIn        => ". Buscado em: {}{}",
        MsgKey::IncludeRelativeTo        => ". Caminho relativo a: {}",
        MsgKey::NativeHasBody            => "Função native \"{}\" não pode ter corpo",
        MsgKey::ForwardHasBody           => "Declaração forward \"{}\" não pode ter corpo",
        MsgKey::DeclNoBody               => "Declaração {} \"{}\" sem corpo. Use \"forward\" para protótipos.",
        MsgKey::VarUnused                => "\"{}\" variável declarada mas não utilizada",
        MsgKey::StockUnused              => "\"{}\" função stock declarada mas não utilizada",
        MsgKey::SymDeprecated            => "\"{}\" está marcado como depreciado",
        MsgKey::SymDeprecatedUsage       => "\"{}\" está depreciado",
        MsgKey::SymFromDeprecatedFile    => "\"{}\" pertence a um include depreciado",
        MsgKey::IncludeDeprecated        => "\"{}\" está depreciado",
        MsgKey::ParamUnused              => "Parâmetro \"{}\" declarado mas não utilizado",
        MsgKey::SymbolUndeclared         => "\"{}\" não está declarado — verifique se o include correto está presente",
        MsgKey::DefineUnused             => "\"{}\" definido mas não utilizado",
        MsgKey::IncludeNoSymbolsUsed     => "\"{}\" incluído mas nenhum de seus símbolos é utilizado",
        MsgKey::TryIncludeNotFound       => "\"{}\" não encontrado — #tryinclude ignorado pelo compilador",
        MsgKey::NativeNeverCalled        => "\"{}\" native declarada mas nunca chamada",
        MsgKey::ForwardNeverCalled       => "\"{}\" forward declarado mas nunca chamado",
        MsgKey::FuncNeverCalled          => "\"{}\" função declarada mas nunca chamada",
        MsgKey::IndentInconsistent       => "Indentação inconsistente: esperado {} colunas, encontrado {}",
        MsgKey::RefsZero                 => "0 referências",
        MsgKey::RefsOne                  => "1 referência",
        MsgKey::RefsMany                 => "{n} referências",
        MsgKey::HoverDeprecated          => "**Depreciado**",
        MsgKey::KwIf                     => "if (condição) { }",
        MsgKey::KwIfElse                 => "if/else",
        MsgKey::KwElse                   => "else",
        MsgKey::KwFor                    => "for (new i = 0; i < n; ++i)",
        MsgKey::KwWhile                  => "while (condição) { }",
        MsgKey::KwDo                     => "do { } while (condição)",
        MsgKey::KwSwitch                 => "switch (valor) { case: }",
        MsgKey::KwCase                   => "case valor:",
        MsgKey::KwDefault                => "default: (switch)",
        MsgKey::KwReturn                 => "return valor",
        MsgKey::KwBreak                  => "break — sai do loop/switch",
        MsgKey::KwContinue               => "continue — próxima iteração",
        MsgKey::KwGoto                   => "goto label",
        MsgKey::KwExit                   => "exit — encerra o script",
        MsgKey::KwNewLocal               => "new variável local",
        MsgKey::KwSizeof                 => "sizeof variável — tamanho",
        MsgKey::KwTagof                  => "tagof variável — tag numérica",
        MsgKey::KwTrue                   => "true (1)",
        MsgKey::KwFalse                  => "false (0)",
        MsgKey::KwCellmax                => "valor máximo de cell",
        MsgKey::KwCellmin                => "valor mínimo de cell",
        MsgKey::KwCellbits               => "bits por cell",
        MsgKey::KwStock                  => "stock function",
        MsgKey::KwPublic                 => "public function (callback)",
        MsgKey::KwForward                => "forward declaração",
        MsgKey::KwNative                 => "native declaração",
        MsgKey::KwStatic                 => "static function/variável",
        MsgKey::KwEnum                   => "enum declaração",
        MsgKey::KwConst                  => "constante global",
        MsgKey::KwNewGlobal              => "variável global",
        MsgKey::KwDefine                 => "#define macro",
        MsgKey::KwUndef                  => "#undef macro",
        MsgKey::KwInclude                => "#include <arquivo>",
        MsgKey::KwTryinclude             => "#tryinclude <arquivo> (se existir)",
        MsgKey::KwIfDefined              => "#if defined MACRO … #endif",
        MsgKey::KwIfdef                  => "#ifdef MACRO … #endif",
        MsgKey::KwIfndef                 => "#ifndef MACRO … #endif",
        MsgKey::KwElseDir                => "#else (dentro de #if)",
        MsgKey::KwEndif                  => "#endif",
        MsgKey::KwPragma                 => "#pragma opção",
        MsgKey::KwAssert                 => "#assert condição (compile-time)",
        MsgKey::KwError                  => "#error mensagem (compile-time)",
        MsgKey::KwWarning                => "#warning mensagem (compile-time)",
        MsgKey::KwAtDeprecated           => "Marca o símbolo seguinte como depreciado",
        MsgKey::KwLocal                  => "local",
    }
}
